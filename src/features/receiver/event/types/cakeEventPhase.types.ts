// 케이크 이벤트 페이지의 진행 단계(Phase)
export type CakeEventPhase =
  /**
   * intro
   * - 페이지 진입 직후 단계
   * - 사용자에게 안내 문구를 약 15초간 표시
   * - 케이크는 아직 직접 노출되지 않으며, 오버레이로 가려진 상태
   * - 사용자 입력(터치)은 불가능
   * - 다음 단계(reveal)로의 전환은 타이머 종료로만 발생
   */
  | "intro"

  /**
   * reveal
   * - 안내 문구가 사라진 직후 단계
   * - 검은 오버레이의 opacity/밝기 애니메이션을 통해
   *   creator가 꾸민 케이크가 점진적으로 드러나는 연출
   * - 촛불 flame은 오버레이보다 위 레이어에서 유지
   * - 사용자 입력은 여전히 불가능
   * - reveal 애니메이션 완료 시 readyToBlow로 전환
   */
  | "reveal"

  /**
   * readyToBlow
   * - 케이크 노출이 완료된 상태
   * - 중앙에 "눌러주세요" 안내 문구와 터치 가능한 영역 표시
   * - 촛불 flame이 켜진 상태로 유지
   * - 사용자의 단일 터치 입력을 대기
   * - 터치 성공 시 blown 단계로 즉시 전환
   */
  | "readyToBlow"

  /**
   * blown
   * - 사용자가 터치를 성공하여 촛불이 꺼진 직후 단계
   * - 촛불 flame을 꺼짐 상태로 전환
   * - 오버레이가 빠르게 밝아지며 불이 켜진 듯한 분위기 연출
   * - 파티클 효과를 1회 실행
   * - 중복 입력을 방지하기 위해 추가 터치는 비활성
   * - 연출 완료 후 done 단계로 전환
   */
  | "blown"

  /**
   * done
   * - 모든 연출이 종료된 최종 단계
   * - 파티 분위기의 안정된 화면 상태
   * - 하단 "다음으로" 버튼이 활성화됨
   * - 이후 페이지 이동 또는 다음 이벤트로 진행 가능
   */
  | "done";
